ActionController::Routing::Routes.draw do |map|
  map.logout '/logout', :controller => 'sessions', :action => 'destroy'
  map.login '/login', :controller => 'sessions', :action => 'new'
  map.register '/register', :controller => 'users', :action => 'create'
  map.signup '/signup', :controller => 'users', :action => 'new'
  map.resources :users, :member => { :password => :get, :update_password => :post }

  map.resources :site_pages
  map.resources :site_settings, :collection => { :intro => :get }

  map.resource :session

  
  map.resources :learning_modules do |learning_module|
    learning_module.resources :sections do |section|
      section.resources :content_pages
      section.resources :quiz_pages, :member => { :score => :get } do |quiz_page|
        quiz_page.resources :quiz_questions, :member => { :get_answer => :post } do |quiz_question|
          quiz_question.resources :quiz_answers
        end
      end
    end
  end

  map.root :controller => "learning_modules"

  # The priority is based upon order of creation: first created -> highest priority.

